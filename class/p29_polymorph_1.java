public class p29_polymorph_1 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println(a + b); // Output: 30

        System.out.println("Sum is : " + a + b); // Output: Sum is: 1020

        System.out.println('a' + 'b'); // Output: 195 (ASCII value)

        System.out.println("a" + 'b'); // ab

        System.out.println('a' + "b"); // ab

        System.out.println('a' + 'b' + "c"); // ab

    }
}
